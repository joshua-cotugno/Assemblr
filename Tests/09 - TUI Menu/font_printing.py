    lines = [""] * 6  # Initialize an empty list for each line of characters

    for char in text:
        if char in font:
            char_lines = font[char].split('\n')
            for i, line in enumerate(char_lines):
                lines[i] += line + "  "  # Concatenate the lines for each character with two spaces as separation
        else:
            for i in range(6):
                lines[i] += " " * 10  # For characters not in the font, add 10 spaces as a placeholder

    for line in lines:
        print(line)

